import { writeFile } from 'node:fs/promises'
import * as regularIcons from '@fortawesome/free-regular-svg-icons'
import * as solidIcons from '@fortawesome/free-solid-svg-icons'
import { Icon } from '@sonolus/core'

const matches = Object.keys(Icon)
    .map((key) => {
        for (const name of formatExportNames(key)) {
            if (isIconDefinition(solidIcons[name])) return { key, name, style: 'solid' }

            const regularName = name.replace(/Hollow$/u, '')
            if (isIconDefinition(regularIcons[regularName])) {
                return { key, name: regularName, style: 'regular' }
            }
        }
    })
    .filter(Boolean)

const regularImports = matches
    .filter(({ style }) => style === 'regular')
    .map(
        ({ name }) =>
            `import { ${name} as ${name}Regular } from '@fortawesome/free-regular-svg-icons/${name}'`,
    )
    .sort((a, b) => a.localeCompare(b))
const solidImports = matches
    .filter(({ style }) => style === 'solid')
    .map(({ name }) => `import { ${name} } from '@fortawesome/free-solid-svg-icons/${name}'`)
    .sort((a, b) => a.localeCompare(b))
const definitions = matches.map(
    ({ key, name, style }) => `    ${key}: ${style === 'regular' ? `${name}Regular` : name},`,
)

await writeFile(
    new URL('../src/core/tag-icons.ts', import.meta.url),
    [
        '// This file is generated by scripts/generate-tag-icons.mjs.',
        '// Do not edit it manually.',
        ...regularImports,
        "import type { IconDefinition } from '@fortawesome/free-solid-svg-icons'",
        ...solidImports,
        '',
        'const tagIconDefinitions: Record<string, IconDefinition> = {',
        ...definitions,
        '}',
        '',
        'export function resolveTagIcon(key: string) {',
        '    return tagIconDefinitions[key]',
        '}',
        '',
    ].join('\n'),
)

function formatExportNames(key) {
    const baseName = `fa${key.charAt(0).toUpperCase()}${key.slice(1)}`
    const names = [baseName, baseName.replace(/Mark/u, 'mark')]

    if (key.startsWith('SortDown')) names.push(`faArrowDown${key.slice('SortDown'.length)}`)
    if (key.startsWith('SortUp')) names.push(`faArrowUp${key.slice('SortUp'.length)}`)

    return names
}

function isIconDefinition(value) {
    return typeof value === 'object' && value !== null && 'icon' in value
}
